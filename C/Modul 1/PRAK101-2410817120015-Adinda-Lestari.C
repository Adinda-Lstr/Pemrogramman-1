#include <stdio.h>
int main(){
    // Mendefinisikan Variable
    int x = 5;
    int y = 7;
    int z = 9;

    // Menampilkan nilai variable
    printf(" Variable x bernilai  %d\n", x);
    printf(" Variable y bernilai  %d\n", y);
    printf(" Variable z bernilai  %d\n", z);

    // Menghitung dan menampilkan hasil penjumlahan
    int jumlah = x + y + z;
    printf("Jumlah variable tersebut adalah %d\n", jumlah);
return 0;
}